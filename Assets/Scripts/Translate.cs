using UnityEngine;
using System.Collections;
using UnityEngine.UI;

public class Translate : MonoBehaviour
{
	Text textComp;
	string id;
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	void Awake()
	{
		textComp = gameObject.GetComponent<Text>();
		id = textComp.text;
	}
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	void Start()
	{
		textComp.text = LanguageManager.instance.GetText(id);
	}
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	void OnEnable()
	{
		LanguageManager.OnLanguageChanged += HandleOnLanguageChanged;
	}
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	void OnDisable()
	{
		LanguageManager.OnLanguageChanged -= HandleOnLanguageChanged;
	}
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void HandleOnLanguageChanged()
	{
		textComp.text = LanguageManager.instance.GetText(id);
	}
}
