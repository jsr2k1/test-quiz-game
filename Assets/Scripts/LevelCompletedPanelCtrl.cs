using UnityEngine;
using UnityEngine.UI;
using System.Collections;

public class LevelCompletedPanelCtrl : MonoBehaviour
{
	public Text textLevelCompleted;
	Animator animator;

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void Awake()
	{
		animator = GetComponent<Animator>();
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	void Start()
	{
		textLevelCompleted.text = LanguageManager.instance.GetText("id_level")+ " " + 
			CategoriesManager.instance.GetCurrentLevelInd().ToString() + " " + LanguageManager.instance.GetText("id_completed") + "!";
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public void ShowPopUp()
	{
		AudioManager.instance.PlayAudio(AudioManager.Audios.LevelComplete);
		animator.SetTrigger("ShowPopUp");
	}
}
